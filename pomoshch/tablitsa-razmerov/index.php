<p class="MsoNormal" style="text-align: justify;"> <font face="Tahoma"><font color="#ff0000"><b>Определение размера обуви: главные правила</b></font> </font></p>

<p class="MsoNormal" style="text-align: justify;"><font face="Tahoma">При покупке обуви в интернет-магазине можно легко ошибиться, так как результатом неточных измерений длины и полноты стопы является несоответствие размера. Чтобы избежать данной ситуации, мы подготовили для Вас несколько полезных рекомендаций по правильному измерению размера обуви. </font></p>

<p class="MsoNormal"><font face="Tahoma"><b>Действие №1</b><img src="/upload/medialibrary/11a/11abb31e27e3ef47189877663c2fd7b2.png" title="1.png" border="0" alt="1.png" width="548" height="409" align="left"  /></font></p>

<p class="MsoNormal" style="text-align: justify;"><font face="Tahoma">Вам потребуется лист бумаги длиной больше размера Вашей стопы, скотч и карандаш. Необходимо приклеить лист бумаги к полу при помощи скотча, поставить ногу на лист бумаги, параллельно полу, и обвести ее строго по контуру карандашом. </font></p>

<p class="MsoNormal"><font face="Tahoma"><b>Действие №2</b></font></p>

<p class="MsoNormal" style="text-align: justify;"><font face="Tahoma">При помощи карандаша начертите параллельные линии, касающиеся выдающихся точек ступни (пальцев и пятки), а также по бокам ступни. Используя линейку, необходимо измерить длину и ширину стопы и записать данные. Подобные измерения повторить для второй стопы. В случае разницы в измерениях необходимо выбрать максимальные. </font></p>

<p class="MsoNormal" style="text-align: justify;"><font face="Tahoma">Из получившихся замеров убрать по 0,47 см. Полученные цифры являются длиной и полнотой Вашей стопы. Округлить результат до целой цифры 0 или 5 (например, 22.2см &asymp; 22.5; 30,8см ≈ 31; 25,4см ≈ 25,5). Данные вычисления используются для определения размера в стандартных таблицах.</font></p>

<p class="MsoNormal"><font face="Tahoma"><b>Советуем Вам проводить измерения в послеобеденное время, поскольку после рабочего дня у большинства людей наблюдается отечность ног. Для выбора туфель, ботинок или сапог нужно надеть носок, в котором Вы планируете носить обувь. </b></font></p>

